package model.exceptions;


/**
 * The LookUpTableException class represents an exception thrown when an issue related to a lookup table occurs.
 * This exception is typically thrown during lookup table operations or manipulations.
 *
 * @author Michael Markov
 * @version v1
 */
public class LookUpTableException extends Exception
{
    /**
     * Constructs a new LookUpTableException with no detail message.
     */
    public LookUpTableException ()
    {
        super();
    }
}
